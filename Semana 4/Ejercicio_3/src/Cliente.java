public class Cliente {

    private Float manzanas;
    private Float peras;

    public void setManzanas(Float manzanas){
        this.manzanas = manzanas;
    }
    public void setPeras(Float peras){
        this.peras = peras;
    }

    public String getPeras(){
        return this.peras;
    }
    public String getManzanas(){
        return this.manzanas;
    }
    
}